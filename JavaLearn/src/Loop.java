 
public class Loop {

	public static void main(String[] args) {
		// while loop through a block of code as specified condition is true 
		
		int i=3;
		while (i<5) {
			System.out.println(i);
			i++;
		}
		
		// dowhile loop,The loop will always be executed at least once, even if the condition is false, because the code block is executed before the condition is tested:
		int j=0;
		do {
			System.out.println(j);
			j++;
			}
		while(j<3);
		
	}
	
	

}
