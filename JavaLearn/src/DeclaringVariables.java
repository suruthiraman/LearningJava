// string-Stores text
// int- Stores wholenummer(123 or -123)
//float- stores floating point(19.99 or -19.99)
//char-Stores single character such as 'a' or 'b'
//boolean-Stores values true or false.

// syntax - type variable = value;


public class DeclaringVariables {
	public static void main (String args[]) {
		String name = "SAKTHIJANANI"; // string
		int myNum = 2606;             //int
		float myFloatNum =5.99f;      //float
		char myLetter ='S';           //char
		boolean myBool = true;        //variables
		
		System.out.println(name);
		System.out.println(myNum);
		System.out.println(myFloatNum);
		System.out.println(myLetter);
		System.out.println(myBool);
	}

}
