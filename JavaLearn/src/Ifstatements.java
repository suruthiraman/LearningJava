
public class Ifstatements {

	public static void main(String[] args) {
		if (40 > 8) {     // if statement to specify a block of java code to be a executed if condition is true 
			System.out.println("40 is greater than 8");
		}
       
		int a = 40; // if condition
		int b= 8;
		if(a>b) {
			System.out.println("40 is greater than 8");
		}
	}

}
