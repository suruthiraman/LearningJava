// to perform math task on numbers
public class JavaMath {
	public static void main (String[] args) {
		
		System.out.println(Math.max(33, 35)); // find highest value of x and y 
		System.out.println(Math.min(55,36)); // find lowest value of x and y
		System.out.println(Math.sqrt(16)); // square root
		System.out.println(Math.abs(-5.3)); // positive value of x 
		System.out.println(Math.random()); // Random Number
		
		int randomNum =(int)(Math.random() * 101); // 0 to 100
		System.out.println(randomNum);
	}
	

}
